#include "libft.h"

void	ft_bzero(void *s, size_t n)

{
	size_t	a;
	char	*str;

	str = (char *) s;
	a = 0;
	if (n > 0)
	{
		while (a != n)
		{
			str[a] = '\0';
			a++;
		}
		a = 0;
	}
	return ;
}
