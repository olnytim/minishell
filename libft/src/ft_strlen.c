#include "libft.h"

size_t	ft_strlen(const char *str)
{
	size_t	a;
	char	*len;

	if (!str)
		return (0);
	a = 0;
	len = (char *)str;
	while (len[a] != '\0')
	{
		a++;
	}
	return (a);
}
