#include "libft.h"

t_list	*ft_lstnew(void *content)

{
	t_list	*frs;

	frs = malloc(sizeof(t_list));
	if (!frs)
		return (0);
	frs -> next = NULL;
	frs -> content = content;
	return (frs);
}
